from typing import List
from collections import deque


class Solution:
    def shortestBridge(self, grid: List[List[int]]) -> int:
        island_bfs_list = deque()
        bfs_list = deque()
        # find first island
        directions = [(1, 0), (-1, 0), (0, 1), (0, -1)]
        found = False
        for y in range(len(grid)):
            for x in range(len(grid[0])):
                if grid[y][x] == 1:
                    found = True
                    break
            if found:
                break
        grid[y][x] = None
        island_bfs_list.append((x , y))
        while island_bfs_list:
            x, y = island_bfs_list.popleft()
            for dir_x, dir_y in directions:
                next_x, next_y = x + dir_x, y + dir_y
                if 0 <= next_x < len(grid[0]) and 0 <= next_y < len(grid):
                    if grid[next_y][next_x] == 1:
                        island_bfs_list.append((next_x, next_y))
                    elif grid[next_y][next_x] == 0:
                        bfs_list.append((next_x, next_y, 1))
                    grid[next_y][next_x] = None

        while bfs_list:
            x, y, step = bfs_list.popleft()
            for dir_x, dir_y in directions:
                next_x, next_y = x + dir_x, y + dir_y
                if 0 <= next_x < len(grid[0]) and 0 <= next_y < len(grid):
                    if grid[next_y][next_x] == 1:
                        return step
                    elif grid[next_y][next_x] == 0:
                        grid[next_y][next_x] = None
                        bfs_list.append((next_x, next_y, step + 1))

